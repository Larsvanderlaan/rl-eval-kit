import torch
import numpy as np
 
 
import numpy as np
from typing import Callable, Tuple

def dualdice_linear(
    S: np.ndarray,
    A: np.ndarray,
    S_next: np.ndarray,
    A_next: np.ndarray,
    feature_fn: Callable[[np.ndarray, np.ndarray], np.ndarray],
    ridge_B: float = 1e-6,
    ridge_C: float = 1e-6,
) -> np.ndarray:
    """
    Closed-form linear DualDICE-style solution for γ = 1 with an arbitrary
    (possibly nonstationary) sampling distribution over transitions.

    We assume we have transitions
        (S_i, A_i, S'_i, A'_i),  i = 1,...,n,
    drawn from some base distribution ν over (s, a, s', a').
    Typically:
        - (S, A) are generated by a behavior policy b under some initial ρ,
        - (S', A') are generated by the target policy π at S'.

    We consider a linear density-ratio-like function
        g_θ(s,a) = φ(s,a)^T θ,
    where φ(s,a) = feature_fn(s, a) ∈ R^d.

    For γ = 1, a natural “Bellman-flow” style quadratic objective is
        min_θ  E_ν[ (g_θ(S,A) - g_θ(S',A'))^2 ]
        s.t.   E_ν[ g_θ(S,A) ] = 1,

    i.e., find a function that is as invariant as possible along the
    (S,A) -> (S',A') dynamics of the target policy, with an empirical
    normalization constraint.

    In the linear case, this yields a closed-form problem with
        B = E[ φ φ^T ],
        A = E[ φ (φ - φ')^T ],
        C = A B^{-1} A^T,
        b = E[φ(S,A)],

    and solution
        θ* = C^{-1} b / (b^T C^{-1} b),

    with ridge regularization on B and C for numerical stability.

    Parameters
    ----------
    S : array-like, shape (n, d_s)
        Current-state samples S_i.

    A : array-like, shape (n, d_a) or (n,)
        Current-action samples A_i.

    S_next : array-like, shape (n, d_s)
        Next-state samples S'_i.

    A_next : array-like, shape (n, d_a) or (n,)
        Next-action samples A'_i, typically drawn from the *target* policy π.

    feature_fn : callable
        Function mapping (S, A) to feature matrix Φ:
            Phi = feature_fn(S, A)  -> shape (n, d_feat).

        It must accept batched inputs S, A of the same leading dimension
        and return a 2D array.

    ridge_B : float, default=1e-6
        Ridge regularization added to B before inverting:
            B_reg = B + ridge_B * I.

    ridge_C : float, default=1e-6
        Ridge regularization added to C before solving for θ:
            C_reg = C + ridge_C * I.

    Returns
    -------
    theta_star : np.ndarray, shape (d_feat,)
        Closed-form parameter vector θ*, defining the density-ratio-like
        estimator g_θ(s,a) = φ(s,a)^T θ*. It is normalized so that the
        empirical average (1/n) ∑_i g_θ(S_i, A_i) ≈ 1 under the given
        sample (i.e., under the empirical base measure ν).

        In a degenerate case where b^T C^{-1} b ≈ 0, this function
        returns θ* = 0 (you may wish to handle that case differently).
    """
    # Compute features for current and next state-action pairs
    Phi = np.asarray(feature_fn(S, A), dtype=np.float64)       # (n, d)
    Phi_p = np.asarray(feature_fn(S_next, A_next), dtype=np.float64)  # (n, d)

    if Phi.ndim != 2:
        raise ValueError(f"feature_fn must return a 2D array, got shape {Phi.shape}")
    if Phi_p.shape != Phi.shape:
        raise ValueError(
            f"feature_fn(S_next, A_next) must have same shape as feature_fn(S, A); "
            f"got {Phi.shape} and {Phi_p.shape}"
        )

    n, d = Phi.shape
    if n == 0:
        raise ValueError("Empty dataset: no transitions provided.")

    # Empirical mean feature for normalization constraint: b = E[φ]
    b = Phi.mean(axis=0)  # shape (d,)

    # Compute B = E[φ φ^T], A = E[φ (φ - φ')^T]
    B = (Phi.T @ Phi) / n            # (d, d)
    A = (Phi.T @ (Phi - Phi_p)) / n  # (d, d)

    # Regularize and factor B
    B_reg = B + ridge_B * np.eye(d)
    try:
        B_chol = np.linalg.cholesky(B_reg)
    except np.linalg.LinAlgError:
        # Fall back to symmetric eigensolver if Cholesky fails
        eigvals, eigvecs = np.linalg.eigh(B_reg)
        eigvals_clipped = np.clip(eigvals, a_min=1e-12, a_max=None)
        B_inv = (eigvecs / eigvals_clipped) @ eigvecs.T
        # C = A B^{-1} A^T
        C = A @ B_inv @ A.T
    else:
        # Solve B X = A^T -> X = B^{-1} A^T using Cholesky (no explicit inverse)
        Y = np.linalg.solve(B_chol, A.T)       # L Y = A^T
        X = np.linalg.solve(B_chol.T, Y)       # L^T X = Y -> X = B^{-1} A^T
        C = A @ X

    # Regularize C and solve C v = b
    C_reg = C + ridge_C * np.eye(d)
    try:
        v = np.linalg.solve(C_reg, b)         # v = C^{-1} b
    except np.linalg.LinAlgError:
        # Extremely ill-conditioned; return zero weights
        return np.zeros_like(b)

    denom = float(b @ v)
    if abs(denom) < 1e-12:
        # Degenerate case: fall back to zero (or customize handling)
        theta_star = np.zeros_like(b)
    else:
        # Enforce empirical normalization b^T θ = 1
        theta_star = v / denom

    return theta_star

 
 